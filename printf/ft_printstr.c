#include "ft_printf.h"

int	ft_printstr(char *s)
{
	int		len;

	len = 0;
	if (!s)
		len += write(1, "(null)", 6);
	else
	{
		while (*s)
			len += ft_printchar(*s++);
	}
	return (len);
}
